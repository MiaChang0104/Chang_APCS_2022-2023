public class SimpleCounterButAForLoop {
    public static void main(String[] args) {
        for (int i = 0; i <= 300; i++) {
            System.out.println(i);
        }
        for (int j = 300; j >= 0; j--) {
            System.out.println(j);
        }
        for (int k = 4798; k <= 5324; k++) {
            System.out.println(k);
        }
        for (int l = 5324; l >= 4798; l--) {
            System.out.println(l);
        } // >=
    }
}
